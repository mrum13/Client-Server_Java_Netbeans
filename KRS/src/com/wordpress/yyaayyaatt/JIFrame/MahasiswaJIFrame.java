    void paket() {
        mk = new BrowseMk().getMk();

        if (mk != null) {
            mk = KRS.getMkService().getID(mk.getId());
            int cadangan = mk.getSks();
            if (bolehTidak(mk)) {
                if ((Integer.valueOf(txtJumlahKrs.getText()) + cadangan) <= 24) {

                    mks1.add(mk);
                    this.isitabelKrs();
                    this.updateJmlhSks();
                    paket();
                } else {
                    JOptionPane.showMessageDialog(this, "SKS melebihi kuota", "Peringatan!", JOptionPane.ERROR_MESSAGE);
                    paket();
                }
            } else {
                JOptionPane.showMessageDialog(this, "mata kuliah sudah diambil", "Peringatan!", JOptionPane.ERROR_MESSAGE);
                paket();
            }

            jButton1.requestFocusInWindow();
        }
    }

    void isitabelKrs() {
        Object[][] data = new Object[mks1.size()][4];
        int x = 0;
        for (Mk mk : mks1) {
            data[x][0] = mk.getKd_mk();
            data[x][1] = mk.getMk();
            data[x][2] = mk.getSks();
            data[x][3] = mk.getTakad();
            x++;
        }
        String[] judul = {"Kode", "Nama", "Sks", "T. Akademik"};
        tableAmbil.setModel(new DefaultTableModel(data, judul));
        jScrollPane1.setViewportView(tableAmbil);
    }

    void updateJmlhSks() {
        int total = 0;
        for (Mk mk : mks1) {
            total = total + mk.getSks();
        }
        txtJumlahKrs.setText(String.valueOf(total));

    }
