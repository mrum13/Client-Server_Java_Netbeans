    void tableByAll(List<Mk> params) {
        Object[][] data = new Object[params.size()][5];
        int x = 0;
        for (Mk mk : params) {
            data[x][0] = mk.getKd_mk();
            data[x][1] = mk.getMk();
            data[x][2] = mk.getSks();
            data[x][3] = mk.getTakad();
            x++;
        }
        String[] judul = {"Kode", "Nama", "Sks", "T. Akademik"};
        tableAmbil.setModel(new DefaultTableModel(data, judul));
        jScrollPane1.setViewportView(tableAmbil);
    }

    void tableBySemester(String smtr) {
        detailKrses = KRS.getDetailKrsService().getDetailKrsBySemester(mahasiswa1, smtr);
        Object[][] data = new Object[detailKrses.size()][5];
        int x = 0;
        for (DetailKrs dk : detailKrses) {
            data[x][0] = dk.getMk().getKd_mk();
            data[x][1] = dk.getMk().getMk();
            data[x][2] = dk.getMk().getSks();
            data[x][3] = dk.getMk().getTakad();
            x++;
        }
        String[] judul = {"Kode Mk", "Mk", "Sks", "Thn Akademik"};
        tableAmbil.setModel(new DefaultTableModel(data, judul));
        jScrollPane1.setViewportView(tableAmbil);
    }

    void isiTabelMk() {
        try {
            detailKrses = KRS.getDetailKrsService().getDetailKrsById(mahasiswa1);
            Object[][] data = new Object[detailKrses.size()][5];
            int x = 0;
            for (DetailKrs dk : detailKrses) {
                data[x][0] = dk.getMk().getKd_mk();
                data[x][1] = dk.getMk().getMk();
                data[x][2] = dk.getMk().getSks();
                data[x][3] = dk.getMk().getTakad();
                x++;
            }
            String[] judul = {"Kode Mk", "Mk", "Sks", "Thn Akademik"};
            tableAmbil.setModel(new DefaultTableModel(data, judul));
            jScrollPane1.setViewportView(tableAmbil);
        } catch (Throwable ex) {
            ex.printStackTrace();
        }
    }

    void addDetail() {
        for (DetailKrs s : detailKrses) {
            Mk mk = new Mk();
            mk.setId(s.getMk().getId());
            mk.setKd_mk(s.getMk().getKd_mk());
            mk.setMk(s.getMk().getMk());
            mk.setSks(s.getMk().getSks());
            mk.setTakad(s.getMk().getTakad());

            mks1.add(mk);
        }
    }

