    boolean bolehTidak(Mk mk) {
        boolean status = true;
        for (int a = 0; a < mks1.size(); a++) {
            if (mks1.get(a).getId() == mk.getId()) {
                status = false;
                break;
            } else {
                status = true;
            }
        }
        return status;
    }

