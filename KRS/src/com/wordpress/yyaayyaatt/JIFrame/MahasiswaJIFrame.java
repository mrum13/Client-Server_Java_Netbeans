    void simpanSks() {
        detailKrses = KRS.getDetailKrsService().getDetailKrsBySemester(mahasiswa.getId(), cmbSmtr.getSelectedItem().toString());
        for (DetailKrs d : detailKrses) {
            KRS.getDetailKrsService().delete(d);
            System.out.println("============================hapus=================================");
        }
        if (mks1 != null) {
            for (Mk mk : mks1) {
                Mk a = KRS.getMkService().getID(mk.getId());
                DetailKrs dt = new DetailKrs();
                dt.setWktu(new Date());
                dt.setMahasiswa(mahasiswa);
                dt.setSemester(cmbSmtr.getSelectedItem().toString());
                dt.setMk(a);

                KRS.getDetailKrsService().insert(dt);
            }
            mahasiswa.setJumlah_krs(Integer.parseInt(txtJumlahKrs.getText()));
            KRS.getMahasiswaService().insert(mahasiswa);
            JOptionPane.showMessageDialog(this, "Data sudah Tersimpan", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
            mks1 = new ArrayList<Mk>();
        } else {
            JOptionPane.showMessageDialog(this, "Tidak ada KRS baru yang diambil", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);

        }
    }
