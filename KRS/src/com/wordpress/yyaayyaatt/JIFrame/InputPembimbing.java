    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (btnEdit.getText().equals("Edit")) {
            btnEdit.setText("Simpan");
            btnHapus.setEnabled(false);
            Enabled(true);
            txtKdPembimbing.requestFocusInWindow();
        } else if (btnEdit.getText().equals("Simpan")) {
            saveOrUpdateData();
            statusAwal();
        }
    }//GEN-LAST:event_btnEditActionPerformed
    void saveOrUpdateData() {
        if (pembimbing == null) {
            pembimbing = new Pembimbing();
        }
        System.out.println("pembimbing = " + pembimbing);
        pembimbing.setKode_pembimbing(txtKdPembimbing.getText());
        pembimbing.setDosen(cmbDosen.getSelectedItem().toString());
        KRS.getPembimbingService().insert(pembimbing);
        this.statusAwal();
    }
