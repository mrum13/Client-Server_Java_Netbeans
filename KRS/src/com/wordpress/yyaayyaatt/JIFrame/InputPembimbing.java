    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        if (btnTambah.getText().equals("Tambah")) {

            txtKdPembimbing.setEnabled(true);
            cmbDosen.setEnabled(true);
            btnTambah.setText("Simpan");
            txtKdPembimbing.requestFocusInWindow();
        } else {
            if (txtKdPembimbing.getText().isEmpty()) {
                JOptionPane.showMessageDialog(InputPembimbing.this, "Isi Semua Data!!", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
            } else {
                String kode=null,nama=null;
                List<Pembimbing>p = KRS.getPembimbingService().getPembimbingByKode(txtKdPembimbing.getText());
                for (Pembimbing p1 : p) {
                    kode = p1.getKode_pembimbing();
                    nama = p1.getDosen();
                }
                if (kode==null && nama ==null) {
                SimpanData();
                statusAwal();
                }else{
                    JOptionPane.showConfirmDialog(InputPembimbing.this, "Kode dan Dosen sudah terdaftar","Peringatan",JOptionPane.ERROR_MESSAGE);
                    
                }
            }
        }
}//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed

        if (pembimbing != null) {
            KRS.getPembimbingService().delete(pembimbing);
            statusAwal();
            JOptionPane.showMessageDialog(this, "Berhasil");
            kosongkanTeks();
            statusAwal();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal!!!");
//            }
        }
}//GEN-LAST:event_btnHapusActionPerformed
