    void isiListDosen() {
        cmbDosen.removeAllItems();
        dosens = krs.KRS.getDosenService().getAll();
        for (Dosen dsn : dosens) {
            cmbDosen.addItem(dsn.getDosen());
        }
    }

    void isiTeks() {
        Pembimbing pemb = pembimbings.get(row);
        txtKdPembimbing.setText(pemb.getKode_pembimbing());
        cmbDosen.setSelectedItem(pemb.getDosen());
    }

    void isiTabel() {
        try {
            pembimbings = KRS.getPembimbingService().getAll();
            Object[][] data = new Object[pembimbings.size()][4];

            int x = 0;
            for (Pembimbing pemb : pembimbings) {
                data[x][0] = pemb.getKode_pembimbing();
                data[x][1] = pemb.getDosen();

                x++;
            }
            String[] judul = {"Kode Pembimbing", "Nama"};
            tableDosen.setModel(new DefaultTableModel(data, judul));
            jScrollPane1.setViewportView(tableDosen);
        } catch (Throwable ex) {
            ex.printStackTrace();
        }
    }

    void statusAwal() {
        this.kosongkanTeks();
        this.isiListDosen();
        isiTabel();
        txtKdPembimbing.setEnabled(false);
        cmbDosen.setEnabled(false);
        btnEdit.setText("Edit");
        btnTambah.setText("Tambah");
    }

