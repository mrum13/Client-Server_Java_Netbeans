void SimpanData() {
        if (pembimbing == null) {
            pembimbing = new Pembimbing();
        }
        pembimbing.setKode_pembimbing(txtKdPembimbing.getText());
        pembimbing.setDosen(cmbDosen.getSelectedItem().toString());
        KRS.getPembimbingService().insert(pembimbing);
        this.statusAwal();

        pembimbing = new Pembimbing();
        JOptionPane.showMessageDialog(null, "Simpan Sukses", "Konfirmasi", JOptionPane.INFORMATION_MESSAGE);
    }

    void klikTable() {
        if (tableDosen.getSelectedRow() >= 0) {
            pembimbing = pembimbings.get(tableDosen.getSelectedRow());
            pembimbing = KRS.getPembimbingService().getID(pembimbing.getId());
            System.out.print("mahasiswa = " + pembimbing);
            loadModelToForm();
        }
    }

    private void loadModelToForm() {
        txtKdPembimbing.setText(pembimbing.getKode_pembimbing());
        cmbDosen.setSelectedItem(pembimbing.getDosen());
    }
